def Glove_demo():
    print("glove demo.......")
    model = api.load("glove-wiki-gigaword-100")  # You can also try 50, 200, 300
    print("Vector for 'sample':")
    print(model['sample'])  # Word vector for 'sample'

    print("\nMost similar to 'sample':")
    print(model.most_similar('sample'))
